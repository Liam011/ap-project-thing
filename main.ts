controller.down.onEvent(ControllerButtonEvent.Pressed, function () {
    if (battle && turnOrder == 1) {
        game.showLongText("You defended", DialogLayout.Bottom)
        if (Math.percentChance(50)) {
            playerStat_DEF += 3
        } else {
            playerStat_DEF += 1
        }
        if (battle == true) {
            EnemyAttack()
        }
    }
})
info.onLifeZero(function () {
    game.gameOver(false)
})
sprites.onOverlap(SpriteKind.Player, SpriteKind.Food, function (sprite, otherSprite) {
    sprites.destroy(otherSprite)
    if (info.life() > 100) {
        info.changeLifeBy(10)
    } else {
        info.setLife(100)
    }
})
info.player2.onLifeZero(function () {
    turnOrder = 1
    battle = false
    music.stopAllSounds()
    if (EnemyInfo == Boss) {
        game.gameOver(true)
    }
    info.changeScoreBy(5)
    sprites.destroy(EnemyInfo)
    sprites.destroy(TurnBasedPlayer)
    sprites.destroy(Attack)
    sprites.destroy(Defend)
    sprites.destroy(Special)
    mySprite.setFlag(SpriteFlag.Ghost, false)
    mySprite.setFlag(SpriteFlag.StayInScreen, false)
    mySprite.setFlag(SpriteFlag.Invisible, false)
    scene.cameraFollowSprite(mySprite)
    tiles.setCurrentTilemap(map)
    tiles.placeOnTile(mySprite, location)
})
function EnemyAttack () {
    turnOrder = 0
    if (Math.percentChance(80)) {
        game.showLongText("The enemy attacks", DialogLayout.Bottom)
        if (Math.percentChance(enemyStat_ACC)) {
            info.changeLifeBy(0 - enemyStat_ATK * 10 / playerStat_DEF)
            pause(500)
        } else {
            game.showLongText("and missed", DialogLayout.Bottom)
        }
        turnOrder = 1
    } else {
        game.showLongText("The enemy uses its special", DialogLayout.Bottom)
        if (Math.percentChance(enemyStat_ACC)) {
            if (playerStat_DEF > 10) {
                playerStat_DEF += -3
                pause(500)
            } else {
                playerStat_DEF += -1
                pause(500)
            }
        } else {
            game.showLongText("and missed", DialogLayout.Bottom)
        }
        turnOrder = 1
    }
}
