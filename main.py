def EnemyAttack():
    global turnOrder, playerStat_DEF
    turnOrder = 0
    if Math.percent_chance(80):
        game.show_long_text("The enemy attacks", DialogLayout.BOTTOM)
        if Math.percent_chance(enemyStat_ACC):
            info.change_life_by(0 - enemyStat_ATK * 10 / playerStat_DEF)
            pause(500)
        else:
            game.show_long_text("and missed", DialogLayout.BOTTOM)
        turnOrder = 1
    else:
        game.show_long_text("The enemy uses its special", DialogLayout.BOTTOM)
        if Math.percent_chance(enemyStat_ACC):
            if playerStat_DEF > 10:
                playerStat_DEF += -3
                pause(500)
            else:
                playerStat_DEF += -1
                pause(500)
        else:
            game.show_long_text("and missed", DialogLayout.BOTTOM)
        turnOrder = 1

