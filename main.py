def on_up_pressed():
    global turnOrder
    if battle and turnOrder == 1:
        game.show_long_text("You attacked the enemy", DialogLayout.BOTTOM)
        if Math.percent_chance(playerStat_ACC):
            info.player2.change_life_by(0 - playerStat_ATK * 10 / enemyStat_DEF)
